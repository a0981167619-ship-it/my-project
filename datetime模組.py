import datetime
satisfy=datetime.date(2026,2,5)#取得日期
print(satisfy)
satisfying=datetime.date(1945,7,7)
print(satisfying)

"""datetime.time()"""
agree=datetime.time(10,14,56)#取得時間
print(agree)
unagree=datetime.time(0,12,7)
print(unagree)

"""datetime.datetime()"""
opposite=datetime.datetime(2026,2,5,10,16,24)#取得日期時間
print(opposite)

protien=datetime.datetime(2025,3,4,0,0,0)
print(protien)

"""datetime.timedelta()"""
a=datetime.timedelta(days=2)#取得時間間隔
print(a)
household=datetime.timedelta(days=10)
print(household)

