abundant=int(input("請輸入整數:"))
print(bin(abundant))#轉成二進位
print(oct(abundant))#轉成八進位
print(hex(abundant))#轉成十六進位
print(float(abundant))#轉成浮點數形式
print(abs(abundant))#取絕對值

